package com.terra.ems.framework.service;

/**
 * 短信服务接口
 *
 * @author dengxueping
 * @since 2026-01-11
 */
public interface SmsService {

    /**
     * 发送验证码到指定手机号
     *
     * @param phoneNumber 手机号
     * @return 是否发送成功
     */
    boolean sendCode(String phoneNumber);

    /**
     * 验证手机号和验证码是否匹配
     *
     * @param phoneNumber 手机号
     * @param code        验证码
     * @return 是否验证通过
     */
    boolean verifyCode(String phoneNumber, String code);

    /**
     * 清除验证码缓存
     *
     * @param phoneNumber 手机号
     */
    void clearCode(String phoneNumber);
}
